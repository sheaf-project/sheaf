def test_runner_imports_members_and_attaches_role_tags(auth_client: httpx.Client):
    job = _post_file(auth_client, _build_zip(_sample_data()))
    drive_import_runner()
    final = wait_for_terminal(auth_client, job["id"])

    assert final["status"] == "complete", final
    assert final["counts"]["members_imported"] == 2, final["counts"]
    # "protector" + "host" -> 2 distinct tags
    assert final["counts"]["tags_imported"] == 2, final["counts"]

    members = auth_client.get("/v1/members").json()
    by_name = {m["name"]: m for m in members}
    assert {"Alpha", "Beta"}.issubset(by_name.keys())

    alpha_tags = auth_client.get(f"/v1/members/{by_name['Alpha']['id']}/tags").json()
    tag_names = {t["name"] for t in alpha_tags}
    assert {"protector", "host"} == tag_names


