def _build_zip(
    data: dict,
    *,
    include_manifest: bool = True,
    include_data: bool = True,
    media: dict[str, bytes] | None = None,
) -> bytes:
    """Pack the data + manifest into a PluralSpace-shape zip in memory."""
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        if include_manifest:
            zf.writestr(
                "manifest.json",
                json.dumps(
                    {
                        "export_date": "2026-06-08T00:33:32+00:00",
                        "system_name": data.get("system", {}).get("name", ""),
                        "format_version": "1.1",
                        "user_email": "tester@example.invalid",
                        "regulation_reference": "GDPR Article 15 / 20",
                    }
                ),
            )
        if include_data:
            zf.writestr("data.json", json.dumps(data))
        for path, blob in (media or {}).items():
            zf.writestr(path, blob)
    return buf.getvalue()


def _post_file(
    client: httpx.Client,
    payload: bytes,
    *,
    idem_key: str | None = None,
    options: dict | None = None,
) -> dict:
    form: dict[str, str] = {
        "source": "pluralspace_file",
        "idempotency_key": idem_key or str(uuid.uuid4()),
    }
    if options is not None:
        form["options"] = json.dumps(options)
    resp = client.post(
        "/v1/imports/file",
        files={"file": ("export.zip", payload, "application/zip")},
        data=form,
    )
    assert resp.status_code == 202, resp.text
    return resp.json()


# --- Preview --------------------------------------------------------------


def test_preview_returns_summary(auth_client: httpx.Client):
    payload = _build_zip(_sample_data())
    resp = auth_client.post(
        "/v1/import/pluralspace/preview",
        files={"file": ("export.zip", payload, "application/zip")},
    )
    assert resp.status_code == 200, resp.text
    body = resp.json()
    assert body["member_count"] == 2
    assert body["format_version"] == "1.1"
    assert body["poll_count"] == 1
    names = {m["name"] for m in body["members"]}
    assert names == {"Alpha", "Beta"}


def test_preview_rejects_non_zip(auth_client: httpx.Client):
    resp = auth_client.post(
        "/v1/import/pluralspace/preview",
        files={"file": ("export.zip", b"not a zip", "application/zip")},
    )
    assert resp.status_code == 400


def test_preview_rejects_missing_manifest(auth_client: httpx.Client):
    payload = _build_zip(_sample_data(), include_manifest=False)
    resp = auth_client.post(
        "/v1/import/pluralspace/preview",
        files={"file": ("export.zip", payload, "application/zip")},
    )
    assert resp.status_code == 400


