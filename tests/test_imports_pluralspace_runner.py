"""End-to-end tests for the PluralSpace import runner.

Builds the export zip inline with sanitised data — the importer
mirrors a `format_version: "1.1"` shape captured from a real
PluralSpace export. Tests cover the runner plumbing, the
preview endpoint, and a handful of mapping decisions worth
locking in:
- Multi-channel chat collapse with channel-name prefix on each
  message body.
- `closes_at: null` polls default to a one-year close window
  with a warning event.
- `thoughts[]` is unsupported and surfaces a warning.
- Roles are imported as tags by default.
"""

